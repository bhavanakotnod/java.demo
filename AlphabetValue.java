package com.Methods;

public class AlphabetValue {
	public static void main(String[] args) {
		asciiValue(66);
		alpha('B');
	}
	public static void asciiValue(int num) {
		System.out.println("ASCII value of given number is =  "+(char)num);
	}
	public static void alpha(char name) {
		System.out.println("ASCII number of given character is =  "+(int)name);
	}
	

}
